fun main() {
    println("Введіть номер місяця: ")
    val n: Int = readln().toInt()
    var k = 0 // k виводиться у будь-якому випадку
    //воно залишиться 0 при вводі неправильного номера
    when (n) {
        2 -> k = 28
        4, 6, 9, 11 -> k = 30
        1, 3, 5, 7, 8, 10, 12 -> k = 31
        else -> println("Нема такого місяця ")
    }
    println("Кількість днів $k")
}
