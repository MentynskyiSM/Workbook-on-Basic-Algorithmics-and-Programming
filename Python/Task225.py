fp = 1; fn = 1
n = 2 #номер останнього числа послідовності}
print("Введіть натуральне число:")
m = int(input())
while fn < m :
     f = fp + fn
     fp = fn; fn = f
     n = n + 1
if fn == m :
    print( m, ' є ', n, '-им числом Фібоначчі')
else:
    print( 'Найближче до ', m, \
        ' справа число Фібоначчі: ', fn)
